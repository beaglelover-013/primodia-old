export function inferWeatherIcon(text: string): 'sun' | 'cloud' | 'rain' | 'snow' | 'moon' {
  if (/\u96ea|\u971c|\u51b0|\u5bd2|\u51b7|\u51bb/.test(text)) return 'snow';
  if (/\u96e8|\u96fe|\u6f6e|\u6e7f|\u96f7|\u66b4/.test(text)) return 'rain';
  if (/\u4e91|\u9634|\u973e|\u6c89\u95f7/.test(text)) return 'cloud';
  if (/\u591c|\u6708|\u6697/.test(text)) return 'moon';
  return 'sun';
}

export function getWeatherPool(monthName: string): PrimordiaWeatherEntry[] {
  return PRIMORDIA_WEATHER_TEMPLATE_BY_MONTH[monthName] ?? [];
}

export function pickWeatherForDay(monthName: string, serialDay: number): PrimordiaWeatherEntry | undefined {
  const pool = getWeatherPool(monthName);
  if (!pool.length) return undefined;
  const seed = Math.abs(Math.floor(serialDay * 1103515245 + monthName.length * 12345));
  return pool[seed % pool.length];
}

export function pickRandomWeather(monthName: string): PrimordiaWeatherEntry | undefined {
  const pool = getWeatherPool(monthName);
  if (!pool.length) return undefined;
  return pool[Math.floor(Math.random() * pool.length)];
}